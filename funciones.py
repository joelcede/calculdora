class Operaciones:

    def sumar(self,a,b):
        sumar = a + b
        return sumar

    def restar(self,a,b):
        restar = a - b
        return restar

    def multiplicar(self,a,b):
        multiplicar = a * b
        return multiplicar

    def dividir(self,a,b):
        dividir = a / b
        return dividir
                             